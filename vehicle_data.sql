/* Delete the tables if they already exist */
drop table if exists Costumer;
drop table if exists Distributer;
drop table if exists Producer;
drop table if exists Supplier;
drop table if exists Brand;
drop table if exists Configuration;
drop table if exists Vehicle;
drop table if exists Model;
drop table if exists Supply;
drop table if exists Produce;
drop table if exists Indent;
drop table if exists Buy;

/* Create the schema for our tables */
create table Costumer(cid int PRIMARY KEY, name text, address text, phone_number text, sex text, income int, birthday date, CONSTRAINT Checksex CHECK (sex='男' or sex='女'));
create table Distributer(distributer_name text PRIMARY KEY, address text, level int);
create table Producer(producer_name text PRIMARY KEY, address text);
create table Supplier(supplier_name text PRIMARY KEY, address text);
create table Brand(brand_name text PRIMARY KEY);
create table Configuration(configuration_id int PRIMARY KEY, color text, power text, v_changer text, zhouju int, abs text, esp text, balloon text, decoration text);
create table Vehicle(vin text PRIMARY KEY, configuration_id int, model_id int);
create table Model(model_id int PRIMARY KEY, model_name text, brand_name text);
create table Supply(sid int PRIMARY KEY, sdate date, stype text, amount int, singleprice int, price int, supplier_name text, model_id int);
create table Produce(pdate date, producer_name text, vin text, PRIMARY KEY (producer_name, vin));
create table Indent(iid int PRIMARY KEY, idate date, outprice int, vin text, distributer_name text);
create table Buy(bid int PRIMARY KEY, bdate date, bprice int, cid int, distributer_name text, vin text);

